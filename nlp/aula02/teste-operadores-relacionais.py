num1 = float( input("digite um numero") )
num2 = float( input("digite outro numero") )

if num1 == num2:
    print("Estes numeros são iguais")

if num1 != num2:
    print("Estes numeros são diferentes")

if num1 > num2:
    print("O primeiro numero é maior que o segundo numero")

if num1 < num2:
    print("O segundo numero é maior que o primero numero")